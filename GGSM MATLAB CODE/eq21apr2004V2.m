gH2C1*gH3C2*gRPP3*rIRPP2*RPIRP-...
gH2C1*gH3C2*gRPP2*rIRPP3*RPIRP))/...
((gH2C2*gP3H3*gRPP2*mC1+gH3C2*gP2H2*gRPP3*mC1-...

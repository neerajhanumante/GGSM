H3=((-gH2C2)*mC1 + gH2C1*mC2)/(gH2C1*gH3C2);

P3=((-gH2C2)*gP3H3*gRPP2*mC1*mIRPRP-gH3C2*gP2H2*gRPP3*mC1*...
mIRPRP+gH2C2*gP2H3*gRPP3*mC1*mIRPRP+...
gH2C1*gP3H3*gRPP2*mC2*mIRPRP-gH2C1*gP2H3*gRPP3*mC2*...
mIRPRP-gH2C1*gH3C2*gRPP3*mIRPRP*mP2+...
